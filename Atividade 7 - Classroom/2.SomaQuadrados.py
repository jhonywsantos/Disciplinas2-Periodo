'''Faça um Programa que leia um vetor com 10 números inteiros, calcule e mostre 
a soma dos quadrados dos elementos do vetor.'''

A = int(input('Digite um valor para o item "1" (apenas números inteiros):'))
B = int(input('Digite o valor para o item "2" (apenas números inteiros):'))
C = int(input('Digite o valor para o item "3" (apenas números inteiros):'))
D = int(input('Digite o valor para o item "4" (apenas números inteiros):'))
E = int(input('Digite o valor para o item "5" (apenas números inteiros):'))
F = int(input('Digite o valor para o item "6" (apenas números inteiros):'))
G = int(input('Digite o valor para o item "7" (apenas números inteiros):'))
H = int(input('Digite o valor para o item "8" (apenas números inteiros):'))
I = int(input('Digite o valor para o item "9" (apenas números inteiros):'))
J = int(input('Digite o valor para o item "10" (apenas números inteiros):'))

SomarQuadrados = {(A*A)+(B*B)+(C*C)+(D*D)+(E*E)+(F*F)+(G*G)+(H*H)+(I*I)+(J*J)}
Resultado = SomarQuadrados
print(f'O resultado da soma de todos os números resulta em: {Resultado}')